//! Define a wrapper struct so that implicit conversion to and from int won't
//! mistakenly happen
struct SneedacityAVCodecID {
   SneedacityAVCodecID(AVCodecIDFwd) = delete;
   SneedacityAVCodecID( SneedacityAVCodecIDValue value ) : value{value} {}
   SneedacityAVCodecIDValue value;
};

inline bool operator == ( SneedacityAVCodecID x, SneedacityAVCodecID y )
{ return x.value == y.value; }

inline bool operator != ( SneedacityAVCodecID x, SneedacityAVCodecID y )
{ return !(x == y); }
